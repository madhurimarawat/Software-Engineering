"""
This module provides basic arithmetic functions.
"""

def add(a, b):
    """
    Return the sum of two numbers.

    Parameters:
    a (int or float): The first number.
    b (int or float): The second number.

    Returns:
    int or float: The sum of a and b.
    """
    return a + b

def subtract(a, b):
    """
    Return the difference between two numbers.

    Parameters:
    a (int or float): The minuend (the number from which another number is subtracted).
    b (int or float): The subtrahend (the number that is to be subtracted).

    Returns:
    int or float: The result of a minus b.
    """
    return a - b